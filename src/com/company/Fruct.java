package com.company;

public class Fruct {
	private double vechime;
	private String gust;
	
	public Fruct(double vechime, String gust) {
		this.vechime = vechime;
		this.gust = gust;
	}

	public double getVechime() {
		return vechime;
	}

	public void setVechime(double vechime) {
		this.vechime = vechime;
	}

	public String getGust() {
		return gust;
	}

	public void setGust(String gust) {
		this.gust = gust;
	}
	
	
}
